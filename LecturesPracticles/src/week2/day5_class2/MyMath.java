package week2.day5_class2;

public class MyMath {
	public void calculateSum() {
		//calculate sum of two numbers
		int n1, n2, n3;
		n1=56;
		n2=45;
		n3=n1+n2;
		System.out.println("Sum : "+n3);
	}
	
	public void calculateDiff() {
		//calculate difference of two numbers
		int n1, n2, n3;
		n1=9;
		n2=6;
		n3=n1-n2;
		System.out.println("Diff : "+n3);
	}
	
}