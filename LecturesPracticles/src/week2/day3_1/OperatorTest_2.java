package week2.day3_1;

public class OperatorTest_2 {

	public static void main(String[] args) {
		//Operators
		//1. Assignment Operator (Simple, Multiple, and Short-hand)
		//2. Arithmetic operators (+, -, *, /, %, pow, sqrt)
		//3. Increment and Decrement Operator (++, --)
		//increase value by 1 (whole number)
		int n1=0;
		n1+=1; //n1=n1+1; //increase by 1
		System.out.println(n1);//1
		n1+=2; //increase by 2
		System.out.println(n1);//3
		n1++; //n1+=1; //increase by 1 //post increment
		System.out.println(n1); //4
		++n1; //increase by 1 //pre increment
		System.out.println(n1); //5
		System.out.println(n1++);//5 //print and increase
		System.out.println(n1); //6
		System.out.println(++n1);//7 //increase and print

		
		//decrease value by 1 (whole number) -- (post, pre)

	}

}
